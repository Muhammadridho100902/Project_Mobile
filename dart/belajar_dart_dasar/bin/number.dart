void main() {
  /// tipe data number
  /**
   * number adalah tipe data angka, dibagi menjadi 2 jenis yaitu int dan double
   * int adalah tipe data bilangan bulat
   * double adalah tipe data bilangan desimal, penulisan koma menggunakan "." bukan "," 
   */
  int number1 = 10;
  double number2 = 10.5;

  print(number1);
  print(number2);

  /**
   * tipe data num
   * tipe data number yang bisa digunakan untuk int dan double
   * contoh dibawah
   */

  num number = 9;
  print(number);
  
  number = 11.5;
  print(number);
}
