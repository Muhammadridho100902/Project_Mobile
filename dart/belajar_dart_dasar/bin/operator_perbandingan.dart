void main(){
  /**
   * biasanya hasilnya itu bool atau true / false
   * == artinya sama dengan
   * != artinya tidak sama dengan
   * > artinya lebih dari
   * < artinya kurang dari
   * >= artinya lebih dari atau sama dengan
   * <= artinya kurang dari atau sama dengan
   */

  print(10 == 10);
  print(10 != 10);
}